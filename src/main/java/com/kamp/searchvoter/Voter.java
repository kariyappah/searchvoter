package com.kamp.searchvoter;

public class Voter {
    private Double slNo;
    private Double partNo;
    private String fullName;
    private String relationName;
    private String address;
    private String qualification;
    private String business;
    private Double age;
    private String sex;
    private String epicNumber;
    private String photo;

    public Voter() {
    }

    public String getQualification() {
        return qualification;
    }

    public void setQualification(String qualification) {
        this.qualification = qualification;
    }

    public Double getSlNo() {
        return slNo;
    }

    public void setSlNo(Double slNo) {
        this.slNo = slNo;
    }

    public Double getPartNo() {
        return partNo;
    }

    public void setPartNo(Double partNo) {
        this.partNo = partNo;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getRelationName() {
        return relationName;
    }

    public void setRelationName(String relationName) {
        this.relationName = relationName;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getBusiness() {
        return business;
    }

    public void setBusiness(String business) {
        this.business = business;
    }

    public Double getAge() {
        return age;
    }

    public void setAge(Double age) {
        this.age = age;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getEpicNumber() {
        return epicNumber;
    }

    public void setEpicNumber(String epicNumber) {
        this.epicNumber = epicNumber;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }
}


